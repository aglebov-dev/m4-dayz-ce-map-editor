"""Страховка от краха на сохранённой раскладке панелей.

Раскладка (`QMainWindow.saveState`) — единственное, что редактор применяет к себе ДО того,
как окно показано. Если состояние оказалось противоречивым, Qt валится внутри `show()`
нативным abort'ом: ни `try/except`, ни проверка возврата `restoreState` не спасают, потому
что падает C++ уже после них. А состояние лежит на диске — значит падает КАЖДЫЙ запуск, и
выйти из этого пользователь сам не может.

Поэтому вокруг восстановления ставится метка-файл: её пишут перед восстановлением и
убирают, когда окно успешно показалось. Метка на старте = прошлый запуск восстановление не
пережил → эта сессия идёт с раскладкой по умолчанию, а негодное состояние выбрасывается.

Версия формата нужна для другого случая: состояние, сохранённое сборкой, где у тулбаров не
было `objectName`, Qt писал с предупреждением и неполным (тулбары в него не попадали).
Такие состояния читать незачем — они опознаются по отсутствию/несовпадению версии."""
from __future__ import annotations

from core.paths import paths

# Растёт, когда меняется состав сохраняемого: старые состояния тогда просто игнорируются.
# 2 — у тулбаров появился objectName (без него Qt их не сохранял вовсе).
LAYOUT_VERSION = 2

_MARKER = "layout_restore.lock"


def _marker_path():
    return paths.appdata / _MARKER


def restore_is_unsafe() -> bool:
    """Прошлый запуск не пережил восстановление раскладки — применять её нельзя."""
    try:
        return _marker_path().exists()
    except Exception:
        return False


def begin_restore() -> None:
    """Пометить, что восстановление началось (и ещё не доказало свою безопасность)."""
    try:
        paths.ensure(paths.appdata)
        _marker_path().write_text("restoring", encoding="utf-8")
    except Exception:
        pass                                     # метка — страховка, а не обязанность


def restore_succeeded() -> None:
    """Окно показалось: раскладка безопасна, метку снимаем."""
    try:
        _marker_path().unlink(missing_ok=True)
    except Exception:
        pass
